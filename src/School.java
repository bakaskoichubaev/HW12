public class School {

    private String nameSchool;
    private int yearOfCreationSchool;
    private String addressSchool;

    public String getNameSchool() {
        return nameSchool;
    }

    public void setNameSchool(String nameSchool) {
        this.nameSchool = nameSchool;
    }

    public int getYearOfCreationSchool() {
        return yearOfCreationSchool;
    }

    public void setYearOfCreationSchool(int yearOfCreationSchool) {
        this.yearOfCreationSchool = yearOfCreationSchool;
    }

    public String getAddressSchool() {
        return addressSchool;
    }

    public void setAddressSchool(String addressSchool) {
        this.addressSchool = addressSchool;
    }
}
