public class University {

    private String nameUniversity;
    private int yearOfCreationUniversity;
    private String addressUniversity;

    public String getNameUniversity() {
        return nameUniversity;
    }

    public void setNameUniversity(String nameUniversity) {
        this.nameUniversity = nameUniversity;
    }

    public int getYearOfCreationUniversity() {
        return yearOfCreationUniversity;
    }

    public void setYearOfCreationUniversity(int yearOfCreationUniversity) {
        this.yearOfCreationUniversity = yearOfCreationUniversity;
    }

    public String getAddressUniversity() {
        return addressUniversity;
    }

    public void setAddressUniversity(String addressUniversity) {
        this.addressUniversity = addressUniversity;
    }
}
